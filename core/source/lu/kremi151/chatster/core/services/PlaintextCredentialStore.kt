package lu.kremi151.chatster.core.services

import lu.kremi151.chatster.api.service.CredentialStore
import java.io.File
import java.io.FileInputStream
import java.io.FileOutputStream
import java.util.*

class PlaintextCredentialStore(
        private val credentialFile: File
): CredentialStore {

    private lateinit var credentials: Properties

    constructor(): this(File("credentials.properties"))

    private fun loadCredentials() {
        credentials = Properties()
        if (!credentialFile.exists()) {
            return
        }
        FileInputStream(credentialFile).use { inputStream -> credentials.load(inputStream) }
    }

    private fun storeCredentials() {
        if (!::credentials.isInitialized) {
            return
        }
        FileOutputStream(credentialFile)
                .use { outputStream -> credentials.store(outputStream, "Generated by Chatster. Please consider using a plugin which encrypts the credential store!") }
    }

    @Synchronized
    override fun storeCredential(name: String, value: String) {
        if (!::credentials.isInitialized) {
            loadCredentials()
        }
        credentials.setProperty(name, value)
        storeCredentials()
    }

    @Synchronized
    override fun readCredential(name: String): String? {
        if (!::credentials.isInitialized) {
            loadCredentials()
        }
        return credentials.getProperty(name, null)
    }

}
